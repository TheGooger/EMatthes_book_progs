countries = ['USA', 'England', 'Indonesia', 'Greece', 'Egypt']
print(countries)
print(sorted(countries)) #в алфавитном, но без изменения
print(countries)
print(sorted(countries, reverse=True)) #в обр. алф., но без изм.
print(countries)
countries.reverse() #в обратном с изм.
print(countries)
countries.reverse() #было до изменения
print(countries)
countries.sort() #в алф. навсегда
print(countries)
countries.sort(reverse=True) #в обр. алф. навсегда
print(countries)

